// 获取睡眠类型（使用与result.js相同的逻辑）
function getSleepType(answers) {
    const scores = {
        typeA: 0,
        typeB: 0,
        typeC: 0,
        typeD: 0
    };

    if (answers.q4 >= 3) scores.typeA += 3;
    if (answers.q10_values) {
        if (answers.q10_values.includes('coffee')) scores.typeA += 2;
        if (answers.q10_values.includes('weekend-sleep')) scores.typeA += 2;
        if (answers.q10_values.includes('makeup-sleep')) scores.typeA += 2;
    }
    if (answers.q1 >= 3) scores.typeA += 1;

    if (answers.q2 === 1) scores.typeB += 3;
    if (answers.q3 >= 3) scores.typeB += 3;
    if (answers.q5 >= 3) scores.typeB += 2;
    if (answers.q1 === 4) scores.typeB += 2;

    if (answers.q11 >= 3) scores.typeC += 3;
    if (answers.q12 >= 2) scores.typeC += 2;
    if (answers.q9 >= 3) scores.typeC += 3;
    if (answers.q3 >= 2) scores.typeC += 1;

    if (answers.q7 >= 3) scores.typeD += 3;
    if (answers.q8 >= 3) scores.typeD += 3;
    if (answers.q5 >= 3) scores.typeD += 2;
    if (answers.q6 >= 3) scores.typeD += 1;

    let maxScore = 0;
    let resultType = 'typeA';

    for (const [type, score] of Object.entries(scores)) {
        if (score > maxScore) {
            maxScore = score;
            resultType = type;
        }
    }

    if (maxScore < 3) {
        const totalScore = Object.values(answers).reduce((sum, val) => {
            if (typeof val === 'number') return sum + val;
            return sum;
        }, 0);

        if (totalScore <= 18) {
            resultType = 'typeA';
        } else if (totalScore <= 28) {
            resultType = 'typeB';
        } else {
            resultType = 'typeC';
        }
    }

    return resultType;
}

// 显示指定章节
function showSection(sectionIndex) {
    const report = reportData[currentType];
    if (!report || !report.sections[sectionIndex]) return;

    const section = report.sections[sectionIndex];

    // 更新内容
    document.getElementById('reportFullContent').innerHTML = section.content;

    // 更新tabs激活状态
    document.querySelectorAll('.report-tab').forEach((tab, index) => {
        if (index === sectionIndex) {
            tab.classList.add('active');
        } else {
            tab.classList.remove('active');
        }
    });

    // 更新导航按钮
    const prevBtn = document.getElementById('prevSection');
    const nextBtn = document.getElementById('nextSection');

    if (sectionIndex === 0) {
        prevBtn.style.display = 'none';
    } else {
        prevBtn.style.display = 'inline-block';
    }

    if (sectionIndex === report.sections.length - 1) {
        nextBtn.textContent = '返回首页';
    } else {
        nextBtn.textContent = '下一章';
    }

    // 滚动到顶部
    window.scrollTo({ top: 0, behavior: 'smooth' });

    currentSectionIndex = sectionIndex;
}

// 初始化报告
function initReport() {
    const answers = JSON.parse(localStorage.getItem('sleepAssessmentAnswers'));

    if (!answers) {
        alert('请先完成睡眠评估');
        window.location.href = 'assessment.html';
        return;
    }

    // 判断睡眠类型
    currentType = getSleepType(answers);
    const report = reportData[currentType];

    // 更新标题
    document.getElementById('reportMainTitle').textContent = report.title;

    // 显示第一章
    showSection(0);

    // 绑定tab点击事件
    document.querySelectorAll('.report-tab').forEach((tab, index) => {
        tab.addEventListener('click', () => {
            showSection(index);
        });
    });

    // 绑定导航按钮
    document.getElementById('prevSection').addEventListener('click', () => {
        if (currentSectionIndex > 0) {
            showSection(currentSectionIndex - 1);
        }
    });

    document.getElementById('nextSection').addEventListener('click', () => {
        if (currentSectionIndex < report.sections.length - 1) {
            showSection(currentSectionIndex + 1);
        } else {
            window.location.href = 'index.html';
        }
    });
}

// 页面加载时初始化
document.addEventListener('DOMContentLoaded', initReport);
